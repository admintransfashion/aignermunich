namespace Trans\ProductRelation\Model\Product\Link\CollectionProvider;

/**
 * Class Conbinations
 */
class Combinations implements \Magento\Catalog\Model\ProductLink\CollectionProviderInterface
{
    /**
     * {@inheritdoc}
     */
    public function getLinkedProducts(\Magento\Catalog\Model\Product $product)
    {
        $products = $product->getCombinationsProducts();

        if (!isset($products)) {
            return [];
        }

        return $products;
    }
}